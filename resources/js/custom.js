function validateName(val) {
	var namereg = /^[a-zA-Z]+ [a-zA-Z]+$/;
	var namereg2 = /^[a-zA-Z]+$/;
	if (namereg.test(val) || namereg2.test(val)) {
		return true;
	}
	else {
		return false;
	}
}

function validatePhone(val) {
	var numreg = /^[a-zA-Z]+$/;
	var empty = /^\s*$/;
	if (numreg.test(val) || empty.test(val)) {
		return false;
	}
	else {
		return true;
	}
}

function isNumber(val) {
	var numreg = /^[0-9]+$/;
	var empty = /^\s*$/;
	if (empty.test(val) || !numreg.test(val)) {
		return false;
	}
	else {
		return true;
	}
}


function checkEmpty(val) {
	var numereg = /^\s*$/;
	if (numereg.test(val)) {
		return true;
	}
	else {
		return false;
	}
}

